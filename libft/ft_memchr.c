#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	ptr--;
	while (n--)
		if (*++ptr == (unsigned char)c)
			return (ptr);
	return (NULL);
}
