#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*c1;
	unsigned char	*c2;
	size_t			i;

	i = 0;
	c1 = (unsigned char *)s1;
	c2 = (unsigned char *)s2;
	if (!n)
		return (0);
	while (c1[i] == c2[i] && i + 1 < n)
		i++;
	return (c1[i] - c2[i]);
}
